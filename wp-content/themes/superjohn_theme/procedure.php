								<p>The decision to proceed with  a vasectomy is a very personal one. So it is important that you have a clear  understanding of what a vasectomy is and what it is not. The following will  provide you with information that will assist you in deciding whether or not a  vasectomy is an appropriate form of contraception for you. </p>
								<h3><strong>What  happens under normal conditions?</strong> <br />
								</h3>
								<p>The testicles produce sperm and testosterone and are located in the scrotum at the  base of the penis. Once produced, the sperm exit the testicle through a  delicate, coiled tube called the epididymis,  where they stay until they are fully matured. Each epididymis is connected to  the prostate by a  tube called the vas deferens.  This muscular tube generally extends from the lower portion of the scrotum into  the inguinal canal (site of most hernias) and then into the pelvis continuing behind the bladder.  It is at this point that the vas deferens joins with the seminal vesicle and forms the ejaculatory duct.  During ejaculation, seminal fluid and seminal vesicles mixes with sperm to form semen which is  expelled through the urethra. <br />
								</p>
								<h3><strong>What  is a vasectomy?</strong> <br />
								</h3>
								<p>A vasectomy is a minor surgical procedure designed to interrupt the sperm  transportation system between the testicle and  the urethra by blocking the vasa deferentia. </p>
								<h3><strong>How  is a vasectomy performed?</strong> <br />
								</h3>
								<p>In general, vasectomies are performed in the office. However, the procedure may  be done at an ambulatory surgery center or in a hospital setting if the patient  and urologist have determined that intravenous sedation is preferable. The decision to proceed in that type of setting may be based  upon the patient's anatomy, anxiety or the need for associated surgical  procedures. <br />
								</p>
								<p>The area will then be washed  with an antiseptic solution. Local anesthesia will be injected to numb the area but the patient will be aware of touch,  tension and movement during the procedure. However, the local anesthetic should  eliminate any sharp pain. The patient is awake during the procedure so, if  necessary, he can let the urologist know if he is experiencing pain so more  local anesthesia can be given. <br />
								</p>
								<p>With a conventional  vasectomy, a urologist makes one or two small cuts in the skin of the scrotum  to access the vas deferens. The vas deferens is cut, and a small piece may be removed leaving a short gap between the two  remaining ends. Next, the urologist may cauterize the lumen or ends of the vas,  then ties the cut ends with suture material. The scrotal incisions may  be closed with dissolvable stitches or allowed to close on its own. The entire  procedure is then repeated on the other side either through the same initial  incision or through a second scrotal incision. <br />
								</p>
								<p>The Urologists at SFVUA are  experts at the <strong>no-scalpel vasectomy</strong>.  The urologist feels for the vas under the  skin of the scrotum and holds it in place with a small clamp. A special  instrument is then used to make a tiny puncture in the skin and stretch the  opening so the vas deferens can gently be lifted out, cut, then tied or clipped  and cauterized and  put back in place. <br />
								</p>
								<h3><strong>What  should the patient expect after a vasectomy?</strong> <br />
								</h3>
								<p>Your urologist should provide you with specific recommendations for your care  after a vasectomy. It is generally wise to return home immediately after the  procedure and avoid strenuous or sexual activity. Swelling and discomfort can  be minimized by placing an ice pack on the scrotum and by  wearing a supportive undergarment, such as a jockstrap. Most patients can  expect to recover completely in less than a week and many are able to return to  their job as early as a day after the procedure. Sexual activity can usually be  resumed within a week following a vasectomy. However, it is important that all  patients recognize that a vasectomy, even though successful, is not effective  immediately. The effectiveness of the vasectomy must be proven by having the  patient submit at least one semen analysis , which demonstrates that there are no sperm in the ejaculate. The  time until disappearance of sperm from the ejaculate varies from patient to  patient. Most urologists do not recommend checking the semen for  sperm for at least three months or 20 ejaculates, whichever comes first. If  sperm continue to be present in the ejaculate, that patient must continue to  use contraception. After waiting for three months or 20 ejaculates, one in five  men will still have sperm in their ejaculate, and will need to wait longer for  the sperm to clear. The patient should not assume that his vasectomy is  effective until his semen analysis demonstrates the absence of sperm. </p>
								<h3><strong>Are  there any risks associated with a vasectomy?</strong> <br />
								</h3>
								<p>In the immediate postoperative period there is the risk of bleeding into the scrotum. If  the patient notices a significant increase in the size of his scrotum or  significant scrotal discomfort, he should contact his urologist immediately. A  patient experiencing fever, scrotal redness or tenderness should also be evaluated  by the surgeon as this may indicate an infection. Discomfort is usually minimal  and should respond to mild analgesics.  More severe pain may indicate infection or other complications. Patients will  often complain of mild lower abdominal discomfort similar to what one would  experience from getting hit in the genitalia. A benign lump,  or granuloma, may  develop because there is a leakage of sperm from the cut end of the vas into  the scrotal tissues. It may occasionally be painful or sensitive to touch or  pressure. <br />
								</p>
								<p>Post-vasectomy pain syndrome  is a chronic pain syndrome that follows vasectomy. The cause of this syndrome  and its incidence are unclear. It is generally treated with anti-inflammatory  agents. Occasionally, patients will elect to undergo vasectomy reversal in an  attempt to alleviate this syndrome. Unfortunately, the response to surgical  intervention is unpredictable. There has been some debate in the past as to  whether vasectomies predispose a man to any future health problems. However,  there is no conclusive evidence that men who have undergone a vasectomy have a  higher risk of cardiovascular disease, prostate cancer, testicular cancer or  other health problems. </p>
								<hr>
								<h2><strong>Frequently Asked Questions:</strong> <br />
								</h2>
								<h3><strong>Can  my partner tell if I have had a vasectomy?</strong> <br />
								</h3>
								<p>There is no significant change in one's ejaculate after a vasectomy since the sperm contributes a small amount to the overall  ejaculate volume. Your partner may on occasion be able to feel the vasectomy  site. This is particularly true if you have developed a granuloma. <br />
								</p>
								<h3><strong>Will  my sense of orgasm be altered by having a vasectomy?</strong> <br />
								</h3>
								<p>Ejaculation and orgasm are generally not affected by vasectomy. The only  exception to this is the occasional patient who has developed post-vasectomy  pain syndrome. <br />
								</p>
								<h3><strong>Can  I become impotent after a vasectomy?</strong> <br />
								</h3>
								<p>An uncomplicated vasectomy cannot cause impotence. <br />
								</p>
								<h3><strong>Can  a vasectomy fail?</strong> <br />
								</h3>
								<p>First, it is important to be certain that a vasectomy has been successful and  that all sperm are absent from the ejaculate prior to stopping other forms of  contraception. Even if the vasectomy has been demonstrated to be effective,  there is a small chance that a vasectomy may fail. This occurs as a result of sperm leaking from one end of the cut vas deferens (the testicular end) and finding a channel to the other end (the abdominal  end). <br />
								</p>

								<h3><strong>Can  something happen to my testicles?</strong> <br />
								</h3>
								<p>Rarely, the testicles may  be injured during a vasectomy as a result of injury to the testicular artery.  Other complications such as a mass of blood (hematoma) or  infection may also affect the testicles. <br />
								</p>
								<h3><strong>Can  I have children after my vasectomy?</strong> <br />
								</h3>
								<p>Yes, but if you have not stored frozen sperm, you will require an additional  procedure. The vas deferens can be microsurgically reconnected (in a procedure  called a vasectomy reversal) to allow normal conception to occur.  Alternatively, sperm can be extracted from the testicle or the epididymis and  utilized for in vitro fertilization. These procedures are costly and may or may not be covered  by insurance. Additionally, they are not successful 100 percent of the time.  Therefore, one should carefully consider nonsurgical alternatives for  contraception prior to deciding to proceed with a vasectomy. <br />
								</p>
